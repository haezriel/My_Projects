

public class VehiclesObject
{
	public int idvehicle;
	public String make;
	public String model;
	public String color;
	public String licensePlate;
	public int mileage;
	
	public VehiclesObject(int idvehicle, String make, String model, String color, String licensePlate, int mileage)
	{
		this.idvehicle = idvehicle;
		this.make = make;
		this.model = model;
		this.color = color;
		this.licensePlate = licensePlate;
		this.mileage = mileage;
	}
}